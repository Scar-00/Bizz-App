    bool DurationEdit(const char *label, int *hours, int *minutes) {
        ImGuiContext& g = *GImGui;
        ImGuiWindow* window = GetCurrentWindow();
        if (window->SkipItems)
            return false;
        const ImGuiID id = window->GetID(label);

        ImGuiStyle& style = g.Style;
        const ImVec2 label_size = CalcTextSize(label, NULL, true);

        bool changed = false;

        char label_buf[64];
        snprintf(label_buf, sizeof(label_buf), "%.2d:%.2d", *hours, *minutes);
        if(ImGui::BeginCombo(label, label_buf, ImGuiComboFlags_NoArrowButton)) {
            ImGui::TextUnformatted("Hours:");
            ImGui::SameLine(100);
            if(ImGui::InputInt("##TimeEditHours", hours, 1, 24, ImGuiInputTextFlags_CharsDecimal)) {
                changed = true;
            }
            ImGui::TextUnformatted("Minutes:");
            ImGui::SameLine(100);
            if(ImGui::InputInt("##TimeEditMinutes", minutes, 1, 10, ImGuiInputTextFlags_CharsDecimal)) {
                changed = true;
            }
            ImGui::EndCombo();
        }

        if(*minutes >= 60) {
            *hours += 1;
            *minutes -= 60;
        }
        if(*minutes <= 0) *minutes = 0;
        if(*hours <= 0) *hours = 0;

        return changed;
    }

